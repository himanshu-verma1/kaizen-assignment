package overloading;

public class sub_overloading {
	//sub() overloading 
	public int sub(int a, int b) {
		return a-b;
	}
	//overloading 
	public float sub(float a , float b) {
		return a-b;
	}
	//overloading 
	public double sub(double a, double b) {
		return a+b;
	}
	//overloading 
	public double sub(double a, int b) {
		return a-b;
	}
	//overloading 
	public float sub(float a, int b) {
		return a-b;
	}
}
