package overloading;

public class multiply_overloading {
	//multiply() overloading 
	public int multiply(int a, int b) {
		return a*b;
	}
	//overloading 
	public float multiply(int a, float b) {
		return a*b;
	}
	//overloading 
	public float multiply(float a, float b) {
		return a*b;
	}
	//overloading 
	public float multiply(float a, float b,float c) {
		return a*b*c;
	}
	//overloading 
	public double multiply(float a , double b) {
		return a*b;
	}
	//overloading 
	public double multiply(double a, double b) {
		return a*b;
	}
	//overloading 
	public double multiply(double a, int b) {
		return a*b;
	}
	
}
