package overloading;

public class division_overloading {
	// division() overloading 
	public int division(int a, int b) {
		return a/b;
	}
	//overloading 
	public float division(float a , float b) {
		return a/b;
	}
	//overloading 
	public double division(double a , double b ) {
		return a/b;
	}
	//overloading 
	public double division(int a , double b ) {
		return a/b;
	}
	//overloading 
	public double division(float a , double b ) {
		return a/b;
	}
	//overloading 
	public float division(float a , int b ) {
		return a/b;
	}
}
